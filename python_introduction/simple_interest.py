# A script to calculate simple interest

Principal = 1000  # P represents the Principal Amount
Rate = 0.05  # R represents the Annual Interest Rate
Time = 3  # T represents the Time in years

# Calculate simple interest using I = P * R * T
Interest_Earned = I = Principal * Rate * Time  # I represents the Interest Earned

# Print the result in a formatted output
print(f"The simple interest is: {Interest_Earned}")
