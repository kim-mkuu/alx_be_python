# A script to calculate simple interest

# Variables definition with meaningful names
principal_amount = 1000  # Initial investment in dollars
annual_interest_rate = 0.05  # 5% interest rate (as a decimal)
investment_duration = 3  # Time in years

# simple interest calculation using the formula I = P * R * T
simple_interest = principal_amount * annual_interest_rate * investment_duration

# Print the result in a formatted output
print(f"The simple interest is: {simple_interest}")